// parseInt
console.log('parseInt("123.dsfgd") :>> ', parseInt("123.dsfgd"));
console.log('parseInt("123.5") :>> ', parseInt("123.5"));
console.log('parseInt("0xAA") :>> ', parseInt("0xAA"));
console.log('parseInt("0xAA",16) :>> ', parseInt("0xAA", 16));
console.log('parseInt("0011",8) :>> ', parseInt("0011", 8));
console.log('parseInt(" 23sf") :>> ', parseInt(" 23sf"));
console.log('parseInt("dd") :>> ', parseInt("dd"));
console.log('parseInt("") :>> ', parseInt(""));

// parseFloat

console.log('parseFloat(" 123.54.6") :>> ', parseFloat(" 123.54.6"));
console.log('parseFloat(" 0011") :>> ', parseFloat(" 0011"));
console.log('parseFloat(" 0x23") :>> ', parseFloat(" 0x23"));
console.log('parseFloat("") :>> ', parseFloat(""));